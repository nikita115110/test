package Magazine.Entity;

public class SelectedItem {

    private int id;
    private byte count;

    public SelectedItem() {
    }

    public byte getCount() {
        return count;
    }

    public void setCount(byte count) {
        this.count = count;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
